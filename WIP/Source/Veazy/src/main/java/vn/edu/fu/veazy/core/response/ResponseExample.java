package vn.edu.fu.veazy.core.response;

public class ResponseExample{
	private String vi;
	private String ja;
	
	public ResponseExample() {
		super();
	}
	public ResponseExample(String vi, String ja) {
		super();
		this.vi = vi;
		this.ja = ja;
	}
	public String getVi() {
		return vi;
	}
	public void setVi(String vi) {
		this.vi = vi;
	}
	public String getJa() {
		return ja;
	}
	public void setJa(String ja) {
		this.ja = ja;
	}
}